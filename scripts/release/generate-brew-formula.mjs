#!/usr/bin/env node
// Emit Formula/zond.rb for the homebrew tap from a release checksums.txt.
// Usage: node scripts/release/generate-brew-formula.mjs <version> <checksums.txt> > zond.rb
import { readFileSync } from 'node:fs'
import { pathToFileURL } from 'node:url'

const REPO = 'kirrosh/zond'
const TARGETS = ['darwin-arm64', 'darwin-x64', 'linux-arm64', 'linux-x64']

export function generateFormula(version, checksumsText) {
  const sums = {}
  for (const line of checksumsText.trim().split('\n')) {
    const [hash, name] = line.trim().split(/\s+/)
    sums[name] = hash
  }
  const sha = (target) => {
    const artifact = `zond-${target}.tar.gz`
    if (!sums[artifact]) throw new Error(`No checksum for ${artifact}`)
    return sums[artifact]
  }
  const url = (target) =>
    `https://github.com/${REPO}/releases/download/v${version}/zond-${target}.tar.gz`

  return `# Auto-generated by the zond release pipeline — do not edit by hand.
class Zond < Formula
  desc "API hygiene scanner — YAML tests from OpenAPI specs, depth checks, drift detection"
  homepage "https://github.com/${REPO}"
  version "${version}"
  license "MIT"

  on_macos do
    on_arm do
      url "${url('darwin-arm64')}"
      sha256 "${sha('darwin-arm64')}"
    end
    on_intel do
      url "${url('darwin-x64')}"
      sha256 "${sha('darwin-x64')}"
    end
  end

  on_linux do
    on_arm do
      url "${url('linux-arm64')}"
      sha256 "${sha('linux-arm64')}"
    end
    on_intel do
      url "${url('linux-x64')}"
      sha256 "${sha('linux-x64')}"
    end
  end

  def install
    bin.install "zond"
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/zond --version")
  end
end
`
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const [version, checksumsPath] = process.argv.slice(2)
  if (!version || !checksumsPath) {
    console.error('usage: generate-brew-formula.mjs <version> <checksums.txt>')
    process.exit(1)
  }
  process.stdout.write(generateFormula(version, readFileSync(checksumsPath, 'utf8')))
}
